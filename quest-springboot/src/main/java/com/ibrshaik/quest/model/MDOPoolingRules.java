package com.ibrshaik.quest.model;

public class MDOPoolingRules {
	
	private int poolSize;
	private int maxRadius;
	private String dummyProviderId;
	/**
	 * @return the poolSize
	 */
	public int getPoolSize() {
		return poolSize;
	}
	/**
	 * @param poolSize the poolSize to set
	 */
	public void setPoolSize(int poolSize) {
		this.poolSize = poolSize;
	}
	/**
	 * @return the maxRadius
	 */
	public int getMaxRadius() {
		return maxRadius;
	}
	/**
	 * @param maxRadius the maxRadius to set
	 */
	public void setMaxRadius(int maxRadius) {
		this.maxRadius = maxRadius;
	}
	/**
	 * @return the dummyProviderId
	 */
	public String getDummyProviderId() {
		return dummyProviderId;
	}
	/**
	 * @param dummyProviderId the dummyProviderId to set
	 */
	public void setDummyProviderId(String dummyProviderId) {
		this.dummyProviderId = dummyProviderId;
	}
	
	
}
